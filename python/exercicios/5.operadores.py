"""
    Cri um programa que peca ao usuario que informe  1 numero(inteiro).
    imprima o numero infrmado, depois incremente uma undade e imprima,  
    depois de cremente uma unidade e imprima
"""

num = int(input("Informe o numero: "))
print(f"O numero digitado  foi: {num}")

num += 1
print(f"Incrementado: {num}")
num -= 1
print(f"decrementado: {num}")